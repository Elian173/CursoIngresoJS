
function mostrar() {

    var ancho;
    var largo;
    var perimetro;

    ancho = prompt("Ingrese el ancho de su rectangulo");
    largo = prompt("ingrese el largo de su rectangulo");

    perimetro = (ancho * 2) + (largo * 2);
    alert("El perimetro de su rectangulo es " + perimetro + ".")



}
